        <h1>Diwali</h1>
        <p>Diwali or Deepavali is the Hindu festival of lights celebrated every year in autumn in the northern hemisphere (spring in southern hemisphere). It is an official holiday in Fiji, Guyana, India, Malaysia, Mauritius, Myanmar, Nepal, Singapore, Sri Lanka, Suriname, and Trinidad and Tobago. On the island of Jamaica, it is celebrated proudly by the Indo-Jamaican community, however in 2010 it was inaugurated as an official yearly event at the historic Devon House residence for the first time, in an effort to celebrate the country's Indian heritage on a national level. One of the most popular festivals of Hinduism, it spiritually signifies the victory of light over darkness, good over evil, knowledge over ignorance, and hope over despair. Its celebration includes millions of lights shining on housetops, outside doors and windows, around temples and other buildings in the communities and countries where it is observed. The festival preparations and rituals typically extend over four to six day period. The word Diwali is used by some communities to mean all the festivities while others think of it as one festival night on the no moon day of the Hindu Lunisolar month Kartika in Bikram Sambat calendar (the month of Aippasi in Tamil Calendar). In the Gregorian calendar, Diwali falls in mid-October and mid-November.</p>
        
		<p>Before Diwali, people clean, renovate, and decorate their homes and offices. During Diwali, people dress up in new clothes or their best outfits, light up diyas (lamps and candles) inside and outside their home, participate in family puja (prayers) to Lakshmi â€“ the goddess of prosperity, light fireworks, engage in family feasts, sharing mithai (sweets), and exchange of gifts between family members and close friends. Diwali also marks a major shopping period in nations where it is celebrated.</p>
		
		<p>The name of festive days as well as the rituals of Diwali vary significantly among Hindus, based on the region of India. In many parts of India, the festivities start with Vasubaras, the day for the cattle, followed by Dhanteras or Dhanatrayodashi (in northern and western part of India). Dhanteras is followed by Naraka Chaturdasi and Laxmi Puja. Laxmi Puja on the no moon day is considered the main day of Diwali in some communities. Next day after the no moon day, is Goverdhan pooja in Northern part of the country. On the same day, in some places, Diwali Padva is celebrated which is dedicated to the relationship of wife and husband. The festivities end with Bhai Dooj dedicated to the bond between sister and brother. Dhanteras usually falls eighteen days after Dussehra.<p>
		
		<p>On the same night that Hindus celebrate Diwali, Jains celebrate a festival also called Diwali to mark the attainment of moksha by Mahavira, Sikhs celebrate Bandi Chhor Divas to mark the release of Guru Hargobind from a Mughal Empire prison, and Newar Buddhists, unlike the majority of Buddhists, celebrate Diwali by worshipping Lakshmi.</p>


